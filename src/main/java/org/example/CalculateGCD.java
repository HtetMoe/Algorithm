public class Main {
    public static void main(String[] args) {
        int gcd = gcd(600, 136);//gcd(37, 14);
        System.out.println("result " + gcd);
    }

    static int gcd(int a, int b) {
        if (b == 0) {
            return a;
        }
        return gcd(b, a % b);
    }
}